package org.javier.agi;

/**
 * AGI Result Hangup.
 */
public class AGIResultHangup extends AGIHangup {

	/** The Constant serialVersionUID. */
	private static final long serialVersionUID = 2909912493041265385L;

	/**
	 * The Constructor.
	 */
	public AGIResultHangup() {
		super();
	}

	/**
	 * The Constructor.
	 * 
	 * @param message
	 *            the message
	 * @param number
	 *            the number
	 */
	public AGIResultHangup(int number, String message) {
		super(number, message);
	}

	/**
	 * The Constructor.
	 * 
	 * @param number
	 *            the number
	 */
	public AGIResultHangup(int number) {
		super(number);
	}

	/**
	 * The Constructor.
	 * 
	 * @param message
	 *            the message
	 * @param cause
	 *            the cause
	 */
	public AGIResultHangup(String message, Throwable cause) {
		super(message, cause);
	}

	/**
	 * The Constructor.
	 * 
	 * @param message
	 *            the message
	 */
	public AGIResultHangup(String message) {
		super(message);
	}

	/**
	 * The Constructor.
	 * 
	 * @param cause
	 *            the cause
	 */
	public AGIResultHangup(Throwable cause) {
		super(cause);
	}

}
